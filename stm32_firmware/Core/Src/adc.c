float VoltageToTemp(float voltage) {
    float R = 154.6f;
    float current_mA = (voltage / R) * 1000.0f;

    float temp_min = -30.0f;
    float temp_max = 50.0f;

    float temp_C = ((current_mA - 4.0f) / 16.0f) * (temp_max - temp_min) + temp_min;

    return temp_C;
}


float FlowmeterGOX(float pressure1, float pressure2) {
    float Cd = 0.8f;
    float d = 0.006f;
    float R = 8.314f;
    float beta = 0.4285f;
    float M = 0.028;
    float T = 300.0f;  // Kelvin olarak sÄ±caklÄ±k

    float area = 3.1416f * (d * d) / 4.0f;
    float deltaP = fabsf(pressure1 - pressure2);
    float den = 1.0f - powf(beta, 4);

    // ğŸ’¥ GÃ¼venlik kontrolÃ¼
    if (pressure1 <= 0.0f || deltaP <= 0.0f || den <= 0.0f) {
        return 0.0f;  // fiziksel anlamda geÃ§ersiz â†’ debi sÄ±fÄ±r
    }

    float rho = (pressure1 * 100000.0f * M) / (R * T);
    if (rho <= 0.0f) return 0.0f;

    float sqrtTerm = (2.0f * deltaP * 100000.0f * rho) / den;
    if (sqrtTerm < 0.0f) return 0.0f;

    float debi = Cd * area * sqrtf(sqrtTerm);
    return debi * 3600.0f;
}


