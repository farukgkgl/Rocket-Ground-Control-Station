float ReadingToADCVoltage(uint32_t adc_raw) {
    uint32_t msb = adc_raw / 100;
    uint32_t lsb = adc_raw % 100;

    uint32_t rounded_lsb = ((lsb + 5) / 10) * 10;

    if (rounded_lsb == 100) {
        rounded_lsb = 0;
        msb += 1;
    }

    uint32_t rounded_adc = msb * 100 + rounded_lsb;

    if (rounded_adc > 4095) {
        rounded_adc = 4095;
    }

    float voltage = (rounded_adc * (3.3f / ADC_MAX));
    voltage = roundf(voltage * 1000.0f) / 1000.0f;

    return voltage;
}
